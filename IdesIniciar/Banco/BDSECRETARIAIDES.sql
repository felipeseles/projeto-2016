    RG CHAR(15) NOT NULL,
    EMISSAO CHAR(2) NOT NULL,
    DATANASC DATE NOT NULL,
    CPF CHAR(14) NULL,
    FILIACAOMAE VARCHAR(50) NULL,
    FILIACAOPAI VARCHAR(50) NULL,
    CEP CHAR(9) NULL,
    ENDERECO VARCHAR(50) NULL,
    NUMERO CHAR(6) NULL,
    COMPLEMENTO VARCHAR(20) NULL,
    BAIRRO VARCHAR(50) NULL,
    CIDADE VARCHAR(50) NULL,
    ESTADO CHAR(2) NULL,
